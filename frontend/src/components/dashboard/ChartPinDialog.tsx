import React, { useState } from 'react';
import { useTranslation } from 'react-i18next';
import { PinnedChart } from '../../services/api';

interface ChartPinDialogProps {
  onClose: () => void;
  onPin: (chart: Partial<PinnedChart>) => Promise<void>;
  locale: string;
}

export const ChartPinDialog: React.FC<ChartPinDialogProps> = ({
  onClose,
  onPin,
  locale,
}) => {
  const { t } = useTranslation('dashboard');

  const [title, setTitle] = useState('');
  const [naturalLanguageQuery, setNaturalLanguageQuery] = useState('');
  const [chartType, setChartType] = useState<'bar' | 'line' | 'pie' | 'table' | 'kpi'>('bar');
  const [refreshInterval, setRefreshInterval] = useState(0);
  const [isSubmitting, setIsSubmitting] = useState(false);
  const [error, setError] = useState<string | null>(null);

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();

    if (!title.trim() || !naturalLanguageQuery.trim()) {
      setError(t('dialog.requiredFields'));
      return;
    }

    setIsSubmitting(true);
    setError(null);

    try {
      await onPin({
        title,
        naturalLanguageQuery,
        chartType,
        refreshInterval,
        sqlQuery: '', // Will be generated by AI
        chartSpec: {},
        position: { row: 0, col: 0, size: 1 },
      });
    } catch {
      setError(t('dialog.pinError'));
      setIsSubmitting(false);
    }
  };

  const chartTypes = [
    { value: 'bar', label: t('chartTypes.bar') },
    { value: 'line', label: t('chartTypes.line') },
    { value: 'pie', label: t('chartTypes.pie') },
    { value: 'table', label: t('chartTypes.table') },
    { value: 'kpi', label: t('chartTypes.kpi') },
  ];

  const refreshOptions = [
    { value: 0, label: t('refreshOptions.manual') },
    { value: 5, label: t('refreshOptions.fiveMinutes') },
    { value: 15, label: t('refreshOptions.fifteenMinutes') },
    { value: 30, label: t('refreshOptions.thirtyMinutes') },
    { value: 60, label: t('refreshOptions.oneHour') },
    { value: 1440, label: t('refreshOptions.oneDay') },
  ];

  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/50">
      <div
        className="bg-white dark:bg-gray-800 rounded-xl shadow-xl max-w-md w-full mx-4 overflow-hidden"
        onClick={(e) => e.stopPropagation()}
      >
        {/* Header */}
        <div className="flex items-center justify-between px-6 py-4 border-b border-gray-200 dark:border-gray-700">
          <h2 className="text-lg font-semibold text-gray-900 dark:text-white">
            {t('dialog.title')}
          </h2>
          <button
            onClick={onClose}
            className="p-1 rounded-lg hover:bg-gray-100 dark:hover:bg-gray-700"
          >
            <svg className="w-5 h-5 text-gray-400" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        </div>

        {/* Form */}
        <form onSubmit={handleSubmit} className="p-6 space-y-4">
          {error && (
            <div className="p-3 bg-red-50 dark:bg-red-900/20 text-red-600 dark:text-red-400 rounded-lg text-sm">
              {error}
            </div>
          )}

          {/* Title */}
          <div>
            <label className="block text-sm font-medium text-gray-700 dark:text-gray-200 mb-1">
              {t('dialog.titleLabel')} *
            </label>
            <input
              type="text"
              value={title}
              onChange={(e) => setTitle(e.target.value)}
              placeholder={t('dialog.titlePlaceholder')}
              className="w-full px-3 py-2 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-900 text-gray-900 dark:text-white focus:ring-2 focus:ring-primary-500 focus:border-transparent"
              dir={locale === 'ar' ? 'rtl' : 'ltr'}
            />
          </div>

          {/* Natural Language Query */}
          <div>
            <label className="block text-sm font-medium text-gray-700 dark:text-gray-200 mb-1">
              {t('dialog.queryLabel')} *
            </label>
            <textarea
              value={naturalLanguageQuery}
              onChange={(e) => setNaturalLanguageQuery(e.target.value)}
              placeholder={t('dialog.queryPlaceholder')}
              rows={3}
              className="w-full px-3 py-2 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-900 text-gray-900 dark:text-white focus:ring-2 focus:ring-primary-500 focus:border-transparent resize-none"
              dir={locale === 'ar' ? 'rtl' : 'ltr'}
            />
          </div>

          {/* Chart Type */}
          <div>
            <label className="block text-sm font-medium text-gray-700 dark:text-gray-200 mb-1">
              {t('dialog.chartTypeLabel')}
            </label>
            <div className="grid grid-cols-5 gap-2">
              {chartTypes.map((type) => (
                <button
                  key={type.value}
                  type="button"
                  onClick={() => setChartType(type.value as typeof chartType)}
                  className={`p-2 rounded-lg border text-center text-sm transition-colors ${
                    chartType === type.value
                      ? 'border-primary-500 bg-primary-50 dark:bg-primary-900/20 text-primary-600 dark:text-primary-400'
                      : 'border-gray-200 dark:border-gray-700 hover:bg-gray-50 dark:hover:bg-gray-700'
                  }`}
                >
                  {type.label}
                </button>
              ))}
            </div>
          </div>

          {/* Refresh Interval */}
          <div>
            <label className="block text-sm font-medium text-gray-700 dark:text-gray-200 mb-1">
              {t('dialog.refreshLabel')}
            </label>
            <select
              value={refreshInterval}
              onChange={(e) => setRefreshInterval(Number(e.target.value))}
              className="w-full px-3 py-2 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-900 text-gray-900 dark:text-white focus:ring-2 focus:ring-primary-500 focus:border-transparent"
            >
              {refreshOptions.map((option) => (
                <option key={option.value} value={option.value}>
                  {option.label}
                </option>
              ))}
            </select>
          </div>

          {/* Actions */}
          <div className="flex justify-end gap-3 pt-4">
            <button
              type="button"
              onClick={onClose}
              className="px-4 py-2 text-gray-700 dark:text-gray-200 hover:bg-gray-100 dark:hover:bg-gray-700 rounded-lg transition-colors"
            >
              {t('dialog.cancel')}
            </button>
            <button
              type="submit"
              disabled={isSubmitting}
              className="px-4 py-2 bg-primary-600 text-white rounded-lg hover:bg-primary-700 disabled:opacity-50 disabled:cursor-not-allowed transition-colors"
            >
              {isSubmitting ? t('dialog.pinning') : t('dialog.pin')}
            </button>
          </div>
        </form>
      </div>
    </div>
  );
};

export default ChartPinDialog;
